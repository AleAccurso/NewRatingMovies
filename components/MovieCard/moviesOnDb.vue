<template>
  <div>
    <UIBigLogo />
    <v-container class="grey lighten-5 mainContainer">
      <v-row no-gutters>
        <v-col v-for="movie in movies" :key="movie.id" cols="12" sm="3">
          <div class="card">
            <v-card>
              <div class="hover10">
                <figure>
                  <img
                    @click="toggleModal(movie)"
                    class="imgMovieCard"
                    :srcset="url + movie.poster_path"
                  />
                  <v-rating
                    v-if="isAuthenticated"
                    class="favoriteMovie"
                    hover
                    length="1"
                    size="40"
                    :empty-icon="emptyIcon"
                    :full-icon="fullIcon"
                    color="yellow darken-3"
                    background-color="grey darken-1"
                  ></v-rating>
                </figure>
              </div>
              <div class="cardInfos">
                <v-card-title v-if="movie.title.length > 22"
                  >{{ movie.title.substring(0, 22) }}...</v-card-title
                >
                <v-card-title v-else>{{ movie.title }}</v-card-title>
                <div v-if="isAuthenticated" class="text-subtitle-1">
                  {{ movie.release_date.substring(0, 4) }}
                </div>
                <div v-else class="text-subtitle-1 mb-2">
                  {{ movie.release_date.substring(0, 4) }}
                </div>
                <v-card-actions>
                  <v-rating
                    v-if="isAuthenticated"
                    background-color="red lighten-3"
                    color="red"
                    hover
                    length="5"
                    size="18"
                    :value="movie.grade / 2"
                    @input="rateMovie($event, movie._id)"
                  ></v-rating>
                </v-card-actions>
              </div>
            </v-card>
          </div>
        </v-col>
      </v-row>
    </v-container>
    <UIBtnTop :showAt="500" />
    <MovieModal
      :revele="revele"
      :toggleModal="toggleModal"
      :movie="movieForModal"
    />
  </div>
</template>
<script>
import axios from "axios";
import MovieModal from "./movieModal";

export default {
  data() {
    return {
      title: "",
      grade: 0,
      url: process.env.API_PIC_URL,
      movies: [],
      emptyIcon: "mdi-heart-outline",
      fullIcon: "mdi-heart",
      sortBy: "title",
      sortDirection: "asc",
      movieForModal: "",
      revele: false,
      baseURL: `${process.env.baseURL}`,
    };
  },
  computed: {
    isAuthenticated() {
      return this.$store.getters.isAuthenticated; // it check if user isAuthenticated
    },
  },
  methods: {
    rateMovie(value, id) {
      axios
        .patch(this.baseURL + "/movies/" + id, {
          grade: value * 2,
        })
        .then(async (response) => {
          this.$store.commit("UPDATE_MOVIE", response.data);
        });
    },
    toggleModal(movie) {
      this.revele = !this.revele;
      this.movieForModal = movie;
    },
  },
  created() {
    this.movies = this.$store.getters.getMovies;
  },
};
</script>
